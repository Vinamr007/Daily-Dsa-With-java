package UdemyTelesko.corejava;

public class Autoboxing_Unboxing {
    public static void main(String[] args) {
        Integer a = 100;   // Autoboxing
        Integer b = 100;   // Autoboxing

        System.out.println(a == b);

        Integer x = 200;
        Integer y = 200;

        System.out.println(x == y);
    }
}
